# Take the string " i love python programming " and:
# 1. Remove extra spaces from both ends
# 2. Convert it to title case 
# 3. Count how many times the string "o" appears


str1 = " i love python programming "

print("After removing extra space from both ends:", str1.strip())
print("Title case:", str1.title())
print("number of times the string \"o\" appears:", str1.count("o"))